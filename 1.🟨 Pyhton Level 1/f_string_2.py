

length = 5
width = 3

# Using f-string to calculate the area of a rectangle
result = f"The area of the rectangle is {length * width} square units."
print(result)


"""
Explanation:
Variables: We have two variables, length and width, with values 5 and 3.
Math inside curly braces: Inside the f-string, {length * width} directly performs the multiplication 
of length and width (5 * 3), resulting in 15.
Resulting string: The final string is "The area of the rectangle is 15 square units." because the math 
operation inside the curly braces is evaluated.
"""